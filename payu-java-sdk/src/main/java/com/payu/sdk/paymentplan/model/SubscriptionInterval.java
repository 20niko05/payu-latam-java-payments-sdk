package com.payu.sdk.paymentplan.model;

/**
 * Represents a subscription interval in the PayU SDK.
 * 
 * @author PayU Latam
 * @since 1.0.0
 * @version 1.0.0, 06/09/2013
 */
public enum SubscriptionInterval {

	/**
	 * Daily subscription
	 */
	DAY,

	/**
	 * Weekly subscription
	 */
	WEEK,

	/**
	 * Monthly subscription
	 */
	MONTH,

	/**
	 * Yearly subscription
	 */
	YEAR
}
